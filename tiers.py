def get_dominio(url: str) -> str:
    """Extrae el dominio limpio de una URL."""
    try:
        d = url.replace("https://", "").replace("http://", "").split("/")[0]
        d = d.lower()
        for prefix in ("www.", "es.", "sg.", "m.", "pe."):
            if d.startswith(prefix):
                d = d[len(prefix):]
        return d
    except Exception:
        return ""


def get_tier(url: str) -> str:
    """Devuelve el tier de un medio según su URL."""
    dominio = get_dominio(url)
    return TIERS.get(dominio, "Sin clasificar")


def es_red_social(url: str) -> bool:
    """True si la URL pertenece a una red social."""
    url_lower = url.lower()
    redes_keywords = [
        "facebook.com", "fb.com", "fb.watch",
        "instagram.com",
        "tiktok.com",
        "twitter.com", "x.com", "t.co/",
        "youtube.com", "youtu.be",
        "linkedin.com",
        "threads.net",
        "reddit.com",
        "pinterest.com",
        "snapchat.com",
        "telegram.org", "t.me/",
        "twitch.tv",
    ]
    return any(r in url_lower for r in redes_keywords)


def orden_tier(tier: str) -> int:
    """Valor numérico para ordenar: I=0, II=1, III=2, Sin clasificar=3."""
    return {"I": 0, "II": 1, "III": 2}.get(tier, 3)
